#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		j;
	char	*ret;

	j = 0;
	ret = (char *)s;
	while (s[j] != '\0')
		j++;
	while (j >= 0)
	{
		if (s[j] == c)
			return (ret + j);
		j--;
	}
	return (0);
}
